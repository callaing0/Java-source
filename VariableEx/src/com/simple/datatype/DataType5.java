package com.simple.datatype;

public class DataType5 {

	public static void main(String[] args) {
		//출력제어 특수문자(이스케이프 문자)
		
		System.out.println("Java ptogram");
		System.out.println("Java\nptogram");
		System.out.print("Java ptogram\n");
		System.out.print("Java\tptogram\n");
		System.out.print("Java\bptogram\n");
		System.out.print("Java\rptogram\n");
		System.out.print("Java ptogram\n");
		
		System.out.println();
		
		// ",'\
		System.out.print("Java \"hello\" ptogram\n");
		System.out.print("Java 'hello' ptogram\n");
		System.out.print("Java \\hello\\ ptogram\n");
		System.out.println("Java ptogram\n");

		
	}

}
